package MaxFrolov_RPIS82;

public class Person {
    private String sName;
    private String fName;

    public Person(String sName, String fName) {
        this.sName = sName;
        this.fName = fName;
    }

    public String getsName() {
        return this.sName;
    }

    public String getfName() {
        return this.fName;
    }
}

